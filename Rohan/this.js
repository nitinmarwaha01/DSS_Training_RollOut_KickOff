//where this keywords is used in javascript
//this keyword in javascript refers to the object it belongs to 
//in the global context, this refers to the global object
//in a function, this refers to the object that called the function
//in an object method, this refers to the object itself
// in a constructor function, this refers to the new object being created

//in arrow function, is not bound to the object, it refers to the lexical scope whereas in normal function it refers to the object that called the function